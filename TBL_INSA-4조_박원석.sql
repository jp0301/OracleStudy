--추가문제. (기본 문제 풀이가 모두 끝난 후 작성한다.)
--          HR계정의 EMPLOYEES 테이블에서 커미션 받는 사람의 수와
--          안받는 사람의 수를 조회한다.
--          출력형태 ---------------
--              구분        인원수
--          커미션받는사원    XXX
--          커미션없는사원    XXX
--          모든사원          XXX

-- HR 계정으로 접속
SELECT CASE GROUPING(T.커미션) WHEN 0
                               THEN T.커미션
                               ELSE '모든사원'
                               END "구분"
       , COUNT(*) "인원수"
FROM ( SELECT CASE WHEN COMMISSION_PCT IS NOT NULL
                   THEN '커미션받는사원'
                   ELSE '커미션없는사원'
                   END "커미션"
       FROM EMPLOYEES ) T
GROUP BY ROLLUP(T.커미션);
