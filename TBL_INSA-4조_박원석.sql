--33. TBL_INSA 테이블에서 BASICPAY + SUDANG 이 
--    100만원 미만,
--    100만원 이상~200만원 미만, 
--    200만원 이상인 직원들의 수 조회.

SELECT DECODE(T.구분, '1', '100만원미만', '2', '100만원이상~200만원미만', '3', '200만원이상', '알수없음') "구분"
     , COUNT(*) "직원수"
FROM ( SELECT CASE WHEN BASICPAY + SUDANG < 1000000
                   THEN '1'
                   WHEN BASICPAY + SUDANG >= 1000000 AND BASICPAY + SUDANG < 2000000
                   THEN '2'
                   WHEN BASICPAY + SUDANG >= 2000000 THEN '3'
                   ELSE '4'
                   END "구분"
       FROM TBL_INSA) T
GROUP BY T.구분;
